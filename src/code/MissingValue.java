public class MissingValue {
    private String valorFaltante;
    
    public MissingValue() {
        this.valorFaltante = "";
    }
    
    public void setValorFaltante(String valorFaltante) {
        this.valorFaltante = valorFaltante;
    }
    
    public String getValorFaltante() {
        return valorFaltante;
    }
}
